int	ft_isprint(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c >= 32 && c <= 127))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
